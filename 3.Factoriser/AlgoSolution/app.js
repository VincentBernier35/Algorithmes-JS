function factoriser(num) {

  for(i = 1; num > 1; num--){
    i = num * i;
  //1 = 5 x 1;
  //5 = 4 x 5;
  //20 = 3 x 20;
  //60 = 2 x 60;
  //120 = 1 x 120;
  }

return i;
}

 

console.log(factoriser(5));



